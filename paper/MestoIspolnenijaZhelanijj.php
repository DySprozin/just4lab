<p>
<i>Здесь&nbsp;же без труда обнаружилось и&nbsp;то, что привело нас в&nbsp;эти старинные стены. На&nbsp;верхней площадке лестницы, перед входом в&nbsp;актовый зал (ныне&nbsp;&mdash; Зал им. А.П.Бородина) в&nbsp;мраморном полу имеется геометрический орнамент, по&nbsp;описаниям полностью повторяющий... Знак Мироздания на&nbsp;загадочном перстне. Совпадает даже цветовое решение орнамента.</i>
</p>

