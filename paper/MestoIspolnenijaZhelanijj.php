В&nbsp;целом архитектура здания не&nbsp;простая, но&nbsp;тщательно продуманная, и&nbsp;поэтому производит сильное впечатление&nbsp;&mdash; использованы как типичные приемы стиля эклектики, так и&nbsp;нестандартные решения. Поражают циклопическая мощь этого сооружения и&nbsp;кажущаяся иррациональность внутреннего устройства, связанная с&nbsp;нарочитой игрой большими объемами.
