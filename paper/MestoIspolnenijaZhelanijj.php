<p>
Масштабы подземных коммуникаций под зданием бывшего Московского промышленного училища не&nbsp;стоит недооценивать&nbsp;&mdash; высота потолка в&nbsp;одном из&nbsp;заброшенных подвалов под Учебным домом достигает двух метров. В&nbsp;одной из&nbsp;ниш стоял забытый лабораторный прибор величиной с&nbsp;холодильник, оставленный здесь, похоже, в&nbsp;начале <span class="nobr">40-х</span> годов прошлого столетия.
</p>

