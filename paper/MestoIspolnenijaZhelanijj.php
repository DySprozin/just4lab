<p>
Свое комплексное исследование этого места мы&nbsp;начали, как водится с&nbsp;подвалов. Под старинными корпусами есть колоссальные подвальные помещения на&nbsp;нескольких уровнях под землю. Но, судя по&nbsp;всему, они никому не&nbsp;нужны. Попасть туда нетрудно даже со&nbsp;стороны Миусской площади (как&nbsp;&mdash; не&nbsp;скажем). Подвалы представляют собой систему из&nbsp;прямых и&nbsp;перпендикулярных коридоров и&nbsp;отходящих от&nbsp;них комнат. Полы в&nbsp;некоторых залиты водой и&nbsp;зеленоватой жижей. Но&nbsp;старинная вентиляция функционирует, а&nbsp;кое-где даже есть свет.
</p>

<p>
Мы&nbsp;бродили по&nbsp;коридорам, спускались на&nbsp;нижние уровни, блудились в&nbsp;тупиках, заходили в&nbsp;загадочные комнаты со&nbsp;сводчатыми потолками.
