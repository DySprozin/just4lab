<p>
Толщина стен&nbsp;&mdash; не&nbsp;менее полутора метров. В&nbsp;здании три этажа, один из&nbsp;них&nbsp;&mdash; цокольный. После солнцепека и&nbsp;жары лицо овевает освежающей прохладой. Атмосфера достаточно спокойная&nbsp;&mdash; ощущение, будто находишься в&nbsp;храме. Освещается все пространство стилизованными под факелы бронзовыми светильниками. Композиция вестибюля в&nbsp;целом получилась, с&nbsp;одной стороны, сдержанной, а&nbsp;с&nbsp;другой&nbsp;&mdash; очень выразительной:
