<p>
Останкино&nbsp;&mdash; одно из&nbsp;самых знаменитых и&nbsp;мистических мест на&nbsp;карте Москвы. На&nbsp;этих землях издавна селились маги и&nbsp;колдуны. По&nbsp;древним преданиям именно здесь был спрятан магический перстень со&nbsp;Знаком Мироздания дающий власть над пространством и&nbsp;временем. Следы этого артефакта неожиданно всплыли в&nbsp;наше время в&nbsp;районе Миусской площади, создав таинственное Место Исполнения Желаний.
</p>
<p>
Первое письменное упоминание о&nbsp;селе Останкино относится к&nbsp;1558&nbsp;году. 
</p>
<p>
Позволим себе привести здесь полный текст легенды об&nbsp;Останкине в&nbsp;том виде, в&nbsp;котором она дошла до&nbsp;наших дней. 
</p>
<div class="quote">
&nbsp;
</div>
<div class="quoting">
<p>
Владел тогда селом боярин Алексей Сатин. Шли у&nbsp;боярина дела успешно, и&nbsp;по&nbsp;хозяйству, и&nbsp;по&nbsp;государственной службе, пока не&nbsp;явилась к&nbsp;нему горбатая старуха в&nbsp;лохмотьях. Стукнула она трижды клюкой о&nbsp;порог боярского дома и&nbsp;погрозила ему пальцем: &laquo;Не&nbsp;распахивай, не&nbsp;тревожь сию землицу! На&nbsp;костях, на&nbsp;останках она древнего люда. Потому и&nbsp;Останкино издавна зовется. А&nbsp;ослушаешься&nbsp;&mdash; быть беде...&raquo;
</p>
<p>
Посмеялся боярин над этими словами, да&nbsp;и&nbsp;прогнал старуху. А&nbsp;через несколько дней по&nbsp;приказу Ивана Грозного Алексей Сатин был схвачен и&nbsp;казнен...
</p>
<p>
Останкино перешло во&nbsp;владение опричника-иноземца. Откуда он&nbsp;родом и&nbsp;как попал в&nbsp;Россию, никто толком не&nbsp;знал. Даже фамилию его в&nbsp;Москве произносили по-разному: то&nbsp;ли Арн, то&nbsp;ли Орн. Люто буйствовал и&nbsp;гулял тот опричник-иноземец в&nbsp;своем новом владении. По&nbsp;ночам затевал с&nbsp;приятелями неведомые на&nbsp;Руси бесовские игрища: выли по-волчьи на&nbsp;Луну, ухали филинами, блеяли козлами и&nbsp;прыгали голые вокруг костра, орали песни, жгли скирды в&nbsp;полях, ловили бродячих собак, привязывали к&nbsp;хвостам зажженные факелы и&nbsp;гнали в&nbsp;окрестные леса, пили вино, смешанное с&nbsp;кровью.
</p>
<p>
А&nbsp;еще стали замечать, что Орн повадился разрывать древние захоронения. Зашептала Москва: ищет опричник-иноземец в&nbsp;тех могилах сокровище, чтобы построить в&nbsp;Останкине великую гору-башню, с&nbsp;которой будет всю Землю видеть, и&nbsp;вещать с&nbsp;нее бесовские мысли всем людям (как мы&nbsp;видим, пророчество сбылось и&nbsp;гора-башня, распространяющая мысли по&nbsp;всей земле красуется в&nbsp;Отанкино, но&nbsp;сейчас не&nbsp;об&nbsp;этом).
</p>
<p>
Тут снова, откуда неведомо, появилась горбатая старуха и&nbsp;погрозила пальцем иноземцу: &laquo;Угомонись, песья голова! Проклято будет Останкино, проклят будет весь твой род...&raquo;
</p>
<p>
И&nbsp;снова прогнали вещунью. А&nbsp;вскоре повстречал Орн заморских купцов. Везли купцы по&nbsp;заказу Ивана Грозного необычный перстень. На&nbsp;перстне том, якобы, отпечатан Знак Мироздания...
</p>
<p>
Не&nbsp;богатство купцов, а&nbsp;именно тайный Знак, отпечатанный на&nbsp;перстне, толкнул опричника-иноземца на&nbsp;страшное преступление: убил он&nbsp;заморских гостей и&nbsp;закопал среди древних могил, там&nbsp;же припрятал и&nbsp;богатство купцов, и&nbsp;тот самый перстень&nbsp;&mdash; до&nbsp;поры, до&nbsp;времени.
</p>
<p>
И&nbsp;все&nbsp;же дознался царь о&nbsp;грехах своего опричника: и&nbsp;о&nbsp;задуманной горе-башне, и&nbsp;об&nbsp;убитых купцах, и&nbsp;о&nbsp;зарытом перстне. Послал своих гонцов в&nbsp;Останкино. Да&nbsp;только исчез Орн. Говорили, будто пропал он&nbsp;в&nbsp;останкинских болотах. Ни&nbsp;купеческие сокровища, ни&nbsp;перстень царские слуги не&nbsp;смогли отыскать...
</p>
</div>
<p>
Легенда эта передавалась из&nbsp;уст в&nbsp;уста, и&nbsp;в&nbsp;последующие столетия предпринималось немало попыток найти загадочный перстень.
</p>
<p>
Интересно, что в&nbsp;последствии перстень с&nbsp;таинственным знаком проявлялся в&nbsp;Останкине в&nbsp;совершенно различных ипостасях. Например, на&nbsp;портрете молодого человека в&nbsp;голубом из&nbsp;знаменитой коллекции графа Шереметева. Откуда появился в&nbsp;коллекции этот портрет, Шереметьев толком и&nbsp;не&nbsp;помнил. На&nbsp;нем был изображен юноша в&nbsp;сине-голубом костюме. На&nbsp;перстне, что красовался на&nbsp;его левой руке, был изображен &laquo;Знак Мироздания&raquo;&nbsp;&mdash; точно такой&nbsp;же, как на&nbsp;перстне, похищенном много лет назад иноземцем Орном.
</p>
<p>
Во&nbsp;время Отечественной войны 1812 года Останкино было занято частями французского генерала Орна. Несколько раз приезжал генерал в&nbsp;имение и&nbsp;заставлял солдат вести раскопки в&nbsp;районе древних захоронений. Местные жители были уверены, что французский генерал являлся потомком опричника-иноземца и&nbsp;искал закопанные своим предком сокровища убитых купцов и&nbsp;спрятанный им&nbsp;перстень. Ни&nbsp;того ни&nbsp;другого генерал не&nbsp;нашел&nbsp;и, разочарованный, уехал из&nbsp;имения. Перед отъездом он&nbsp;приказал принести к&nbsp;нему портрет человека в&nbsp;голубом. После бегства французов портрет снова занял свое место в&nbsp;зале, но... перстень на&nbsp;нем изменил свой облик&nbsp;&mdash; Знак Мироздания с&nbsp;него исчез... Портрет человека в&nbsp;голубом в&nbsp;настоящее время пребывает в&nbsp;запасниках музея-усадьбы Останкино. В&nbsp;экспозицию он&nbsp;не&nbsp;выставляется, смотрители и&nbsp;экскурсоводы не&nbsp;любят о&nbsp;нем говорить.
</p>
<p>
Как ни&nbsp;странно, до&nbsp;наших дней дошли довольно точные описания &laquo;Знака Мироздания&raquo;. Изображенный на&nbsp;перстне символ представлял собой плоский правильный восьмиугольник из&nbsp;мутного красноватого камня, вписанный в&nbsp;восьмиконечную звезду, полученную из&nbsp;наложенных друг на&nbsp;друга двух квадратов темного-серого и&nbsp;красноватого цветов. Вся композиция была заключена в&nbsp;квадрат-рамку черного камня. Никаких украшений в&nbsp;виде драгоценных камней и&nbsp;дополнительных орнаментов перстень не&nbsp;имел.
</p>
<p>
Кто его создал? Какие цели преследовал? И&nbsp;почему так и&nbsp;не&nbsp;решился ни&nbsp;применить его ни&nbsp;уничтожить? &mdash;&nbsp;все это пока остается неразрешимой загадкой.
</p>
<p>
Символы, собранные воедино в&nbsp;этом знаке, широко использовались и&nbsp;используются в&nbsp;разрозненном виде в&nbsp;каббале и&nbsp;других различных мистических учениях и&nbsp;культах. Правильный восьмиугольник изначально представляет собой раннехристианский символ купели&nbsp;&mdash; полного обновления и&nbsp;райского блаженства. В&nbsp;восточных практиках это модель Вселенной с&nbsp;позиции микрокосмоса, где каждый сектор восьмиугольника отвечает за&nbsp;свою часть бытия.
</p>
<p>
Восьмиконечная звезда, образованная наложением друг на&nbsp;друга двух квадратов&nbsp;&mdash; безусловный знак Высшей Силы. Одно время он&nbsp;использовался как символ Бога Саваофа (Бога Отца, Бога сил, воинств). В&nbsp;русской иконописи и&nbsp;христианской православной символике дониконианских времен он&nbsp;фигурирует с&nbsp;некоторым искажением, особенно в&nbsp;период с&nbsp;XIV по&nbsp;XVI&nbsp;в. Этот знак изображался либо в&nbsp;верхней части иконы (чаще всего в&nbsp;правом верхнем углу), либо вместо нимба или как фон над головой Господа Бога Саваофа. В&nbsp;русской традиции звезда восьмиконечная символизировала восемь тысячелетий (&laquo;семь веков Творца и&nbsp;будущего века Отца&raquo;), но&nbsp;в&nbsp;конце XIX&nbsp;&mdash; начале XX&nbsp;в. была признана еретической с&nbsp;точки зрения канонов официального православия.
</p>
<p>
Во&nbsp;все последующие века предпринимались и&nbsp;предпринимаются попытки отыскать таинственный перстень. В&nbsp;конце XIX века поиском перстня со&nbsp;Знаком Мироздания плотно занимался московский архитектор, Максим Карлович Геппенер. Его имя ныне известно только специалистам, а&nbsp;между тем это был достаточно крупный и&nbsp;очень талантливый архитектор, много строивший в&nbsp;Москве. Биографическая справка.
</p>
<p>
<b>Максим Карлович (в&nbsp;масонских метрических циркулярах Вильгельм-Эдуард Макс) Геппенер или Гепнер <span class="nobr">(1848&ndash;1924)</span></b>&nbsp;&mdash; российский архитектор немецкого происхождения. Работал в&nbsp;Москве на&nbsp;различные муниципальные предприятия. Главный архитектор коммунальных служб города в&nbsp;период их&nbsp;строительства при Н.&nbsp;А.&nbsp;Алексееве. Учитель и&nbsp;впоследствии соавтор известного масона И.&nbsp;А.&nbsp;Иванова-Шица. Родился в&nbsp;Германии, учился в&nbsp;Политехникуме в&nbsp;Карлсруэ, работал на&nbsp;постройке общественных зданий в&nbsp;Бадене.
</p>
<ul>
<li> В&nbsp;1870 нанялся в&nbsp;помощники к&nbsp;московскому архитектору А.&nbsp;С.&nbsp;Каминскому, на&nbsp;следующий год получил звание классного художника в&nbsp;Императорской Академии художеств.<br>
<li> C&nbsp;1873 до&nbsp;окончания карьеры&nbsp;&mdash; работал по&nbsp;муниципальным заказам города Москвы.<br>
<li> <span class="nobr">1873&ndash;1914 &mdash;</span> член Технического совета Московской городской управы<br>
<li> <span class="nobr">1876&ndash;1879 &mdash;</span> главный архитектор Воспитательного дома<br>
<li> <span class="nobr">1879&ndash;1893 &mdash;</span> главный архитектор Николаевского приюта<br>
<li> <span class="nobr">1887&ndash;1814 &mdash;</span> главный архитектор городских гимназий<br>
<li> <span class="nobr">1905&ndash;1906 &mdash;</span> председатель Московского архитектурного общества
</ul>
<p>
Строил много школ, в&nbsp;том числе здания Инженерного училища путей сообщения (МИИТ) на&nbsp;нынешней улице Образцова. Важнейшие постройки Геппенера связаны с&nbsp;реконструкцией коммунального хозяйства и&nbsp;трамвайной сетью Москвы в&nbsp;<span class="nobr">1880-е</span> и&nbsp;<span class="nobr">1890-е</span> гг. Венцом его творчества считают постройки важнейших сооружений московского водопровода и&nbsp;канализации.
</p>
<p>
Визитной карточкой М.&nbsp;К.&nbsp;Геппенера специалисты считают Крестовские водонапорные башни. В&nbsp;этих сооружениях лучше всего проявилось стремление архитектора придать красоту и&nbsp;возвышенность утилитарным сооружениям, призванным обслуживать нужды города. Башни находились недалеко от&nbsp;Рижского вокзала, были похожи на&nbsp;средневековую крепость и&nbsp;просматривались даже с&nbsp;Воробьевых гор. В&nbsp;Крестовских водонапорных башнях помимо резервуаров для воды находились учреждения московского водопровода и&nbsp;квартиры служащих. К&nbsp;сожалению, посмотреть на&nbsp;них сегодня нельзя, в&nbsp;1939 году они были взорваны.
</p>

