<p>
Несмотря на&nbsp;то, что не&nbsp;существует прямых документальных данных о&nbsp;посвящении Геппенера, явно подтверждено, что он&nbsp;был масоном, поскольку сохранились сведения о&nbsp;его регулярных посещениях заседаний известной ложи &laquo;Астрея&raquo;. Кроме &laquo;Астреи&raquo; он&nbsp;посещал еще ложи &laquo;Гармония&raquo; и&nbsp;&laquo;Урания&raquo;. Известно также, что одной из&nbsp;целей &laquo;Астреи&raquo; был сбор по&nbsp;всей России различных артефактов власти. Понятно, что перстень со&nbsp;Знаком Мироздания интересовал эту ложу особо.
</p>

<p>
Много тайн хранят здания, построенные Геппенером по&nbsp;мотивам средневековых рыцарских замков и&nbsp;ратуш. Много легенд связано с&nbsp;ними... Архитектор Геппенер часто работал по&nbsp;коммунальным заказам, и&nbsp;даже промышленные здания проектировал со&nbsp;вкусом. Его постройки всегда узнаваемы&nbsp;&mdash; они из&nbsp;красного кирпича, отделанные серым мрамором и&nbsp;гранитом. Он&nbsp;вообще был ярким представителем так называемого &laquo;кирпичного стиля&raquo;.
</p>

<p>
Кирпич позволял создавать нарядные фактурные фасады, где при оформлении использовались декоративные элементы и&nbsp;композиционные приемы византийской, средневековой европейской и&nbsp;русской архитектуры. Этот архитектурный стиль называют то&nbsp;неоготическим, то&nbsp;псевдо-романским, то&nbsp;краснокирпичной эклектикой.
</p>

<p>
Все постройки Геппенера так или иначе отвечают поздней масонской символике, хотя в&nbsp;их&nbsp;орнаментальных украшениях она встречается довольно редко&nbsp;&mdash; здесь она как&nbsp;бы замаскирована, иногда вмурована в&nbsp;стены и&nbsp;пол, подальше от&nbsp;посторонних глаз. К&nbsp;концу XIX века символика &laquo;вольных каменщиков&raquo; и&nbsp;&laquo;тайных зодчих&raquo; безнадежно устарела и&nbsp;вышла из&nbsp;моды&nbsp;&mdash; масоны сменили эстетику формы на&nbsp;стратегию содержания.
</p>

<p>
Всевозможные циркули, мастерки, угольники, отвесы и&nbsp;прочую инженерно-строительную атрибутику на&nbsp;фасадах домов сменили артефакты с&nbsp;направленной силой воздействия, надежно спрятанные в&nbsp;стенах или в&nbsp;полах новых зданий. В&nbsp;применении масонской символики начиналась новая эра.
</p>

<p>
Очередная московская легенда указывает на&nbsp;то, что в&nbsp;одном из&nbsp;сохранившемся зданий, принадлежащих &laquo;краснокирпичному перу&raquo; Максима Геппенера, существует место, где исполняются желания. По&nbsp;одной из&nbsp;версий в&nbsp;пол был вмурован один из&nbsp;сильнейших в&nbsp;истории человечества мистических предметов&nbsp;&mdash; обнаруженный в&nbsp;Останкино перстень со&nbsp;Знаком Мироздания.
</p>

<p>
Реликвию нашел совершенно случайно один из&nbsp;потомков крепостных графа Шереметьева, разнорабочий Иван Тюнин&nbsp;&mdash; когда укрепляли фундамент восточного флигеля усадьбы и&nbsp;произвели боковой подкоп, лопата наткнулась на&nbsp;древний младенческий гробик, который тут&nbsp;же развалился. Среди полуистлевших косточек лежал массивный перстень с&nbsp;тайным знаком и&nbsp;крупным красным камнем по&nbsp;центру. История этого странного захоронения уже тогда было овеяна глубокой тайной. Раскрыть ее&nbsp;в&nbsp;наше время тем более не&nbsp;возможно.
</p>

<p>
Иван быстро спрятал находку, а&nbsp;останки закопал поглубже&nbsp;&mdash; возможно, там они и&nbsp;покоятся и&nbsp;по&nbsp;сей день. Вечером, сидя на&nbsp;завалинке, он&nbsp;размышлял&nbsp;&mdash; как и&nbsp;кому выгодно сбыть находку. Подошла к&nbsp;нему древняя простоволосая старуха в&nbsp;грязных лохмотьях и&nbsp;прошамкала:<br>
<br>
&mdash;&nbsp;Брось кольцо, Ванька! Брось в&nbsp;колодец, что на&nbsp;задах усадьбы барской, не&nbsp;то&nbsp;быть беде!<br>
<br>
Известно, что Тюнин ослушался. На&nbsp;следующий день он&nbsp;уже был на&nbsp;знаменитом Хитровом рынке, где показал перстень известному в&nbsp;Москве скупщику краденых драгоценностей Мишке Свищу. Свищ внимательно осмотрел украшение и&nbsp;попросил нашедшего заглянуть на&nbsp;другой день&nbsp;&mdash; он&nbsp;приведет покупателя.
</p>

<p>
Покупатель оказался почтенным, богато одетым господином. Увидев перстень и&nbsp;узнав место его обретения, он&nbsp;буквально потерял дар речи и&nbsp;купил его, не&nbsp;торгуясь. Господина звали Максим Карлович Геппенер. Несомненно, он&nbsp;понял, что к&nbsp;нему в&nbsp;руки попал древний останкинский артефакт, за&nbsp;которым не&nbsp;одно столетие охотились масонские ложи всего мира. Что было дальше&nbsp;&mdash; известно очень отрывочно.
</p>

<p>
Труп Ивана Тюнина был найден через несколько дней в&nbsp;колодце на&nbsp;задах Останкинской усадьбы. К&nbsp;Геппенеру в&nbsp;тот&nbsp;же день явилась таинственная старуха&nbsp;и, напугав изрядно, наказала избавиться от&nbsp;перстня.
По&nbsp;сохранившемуся приданию, Максим Геппенер замуровал таинственное украшение на&nbsp;площадке &laquo;Царской лестницы&raquo; одного из&nbsp;возведенных им&nbsp;в&nbsp;тот период зданий. Исследовательская группа &laquo;Новый Космопоиск&raquo; попробовала разобраться в&nbsp;подробностях легенды и&nbsp;найти это место.
</p>

<p>
Прежде всего, необходимо было выяснить причину названия лестницы&nbsp;&mdash; &laquo;Царская&raquo;. В&nbsp;Союзе архитекторов России нам объяснили, что такое название для парадных лестниц действительно фигурировало в&nbsp;Москве в&nbsp;качестве неофициального. Обозначало оно посещение того или иного здания Государем Императором и&nbsp;собственно лестницу, по&nbsp;которой он&nbsp;шествовал.
</p>

<p>
Около десятка творений выдающегося зодчего Максима Геппенера, выдержав испытание временем и&nbsp;советской властью, продолжают украшать наш город:
</p>

<ul>
<li> 1874&nbsp;&mdash; Особняк купца Ф.&nbsp;М.&nbsp;Наживина (Садовническая ул., 19, корп.&nbsp;1)
<li> 1884&nbsp;&mdash; Сокольническая полицейская часть с&nbsp;пожарной каланчой
<li> 1889&nbsp;&mdash; Здание лютеранского детского приюта в&nbsp;Гороховском переулке (ныне&nbsp;&mdash; Школа акварели Сергея Андрияки)
<li> 1893&nbsp;&mdash; Московский газовый завод и&nbsp;газгольдеры за&nbsp;Курским вокзалом
<li> 1895&nbsp;&mdash; Насосная станция за&nbsp;Новоспасским мостом (ныне&nbsp;&mdash; Музей воды)
<li> 1895&nbsp;&mdash; Приют на&nbsp;углу Большого Харитоньевского и&nbsp;Малого Козловского переулков
<li> 1898&nbsp;&mdash; Здание Московского инженерного училища (ныне МИИТ)
<li> 1901&nbsp;&mdash; Трамвайные подстанции на&nbsp;Краснопрудной улице и&nbsp;в&nbsp;Сокольниках
<li> 1903&nbsp;&mdash; Доходный дом А.&nbsp;С.&nbsp;Хомякова, ул. Кузнецкий Мост, 6
<li> 1903&nbsp;&mdash; Здание Московского промышленное училища (ныне&nbsp;&mdash; РХТУ им. Д.И. Менделеева)
</ul>

<p>
По&nbsp;непонятным причинам ни&nbsp;одно из&nbsp;них до&nbsp;сих пор не&nbsp;признано памятником архитектуры, довольствуясь мутным статусом &laquo;невыявленный объект культурного наследия&raquo;, позволяющим пустить здание под снос в&nbsp;любой момент.
</p>

<p>
При разрушении построек Геппенера отмечалось присутствие загадочной символики, регистрировалось наличие неких артефактов (например, вмурованная в&nbsp;стену изысканная женская головка&nbsp;&mdash; стилизованный портрет легендарной Лорелеи), но&nbsp;информация о&nbsp;перстне со&nbsp;Знаком Мироздания не&nbsp;проходила нигде, даже в&nbsp;виде слухов. Это позволяет предположить, что таинственная реликвия все еще ждет своего часа в&nbsp;одном из&nbsp;сохранившихся зданий. Вопрос&nbsp;&mdash; в&nbsp;каком именно.
</p>

<p>
Дошедшие до&nbsp;наших дней здания талантливого архитектора пока не&nbsp;раскрывают своих тайн. Однако легенда о&nbsp;загадочном перстне и&nbsp;русском архитекторе немецкого происхождения датирует события того времени относительно точно&nbsp;&mdash; первые годы&nbsp;ХХ века&nbsp;&mdash; что сужает поисковое поле до&nbsp;четырех сохранившихся объектов. Обе трамвайные подстанции отметаются сразу&nbsp;&mdash; ввиду исключительной утилитарности этих построек и&nbsp;отсутствия парадных лестниц. Доходный дом А.&nbsp;С.&nbsp;Хомякова тоже не&nbsp;подходит под заданные критерии&nbsp;&mdash; лестницы в&nbsp;нем трудно назвать парадными. И, наконец, основная причина&nbsp;&mdash; полное отсутствие исторических данных о&nbsp;посещении этих заведений августейшими особами.
</p>

<p>
Стало быть, речь может идти только об&nbsp;одном из&nbsp;сохранившихся зданий работы Максима Геппенера&nbsp;&mdash; Московском промышленном училище. Основано оно было Высочайшим указом и&nbsp;14&nbsp;апреля 1903 года удостоилось посещения Императорской семьей (данные с&nbsp;официального сайта РХТУ). Нашей исследовательской группой было принято решение об&nbsp;исследовании именно этого здания. Стоит посвятить несколько слов истории места, на&nbsp;котором расположился комплекс зданий бывшего Московского промышленного училища, ныне&nbsp;&mdash; РХТУ им. Д.И. Менделеева.
</p>

<p>
Москва, как известно, стоит на&nbsp;так называемой Русской платформе, довольно прочном геологическом образовании. Но&nbsp;у&nbsp;каждой платформы есть свои разломы, и&nbsp;один из&nbsp;них, судя по&nbsp;специальным картам, весьма не&nbsp;большой, проходит по&nbsp;Миусской площади как раз под корпусами РХТУ им. Д.И. Меделеева, определяя некоторую геопатогенность этой зоны. Эти здания имеют непростую историю, они овеяны легендами и&nbsp;мистическими традициями с&nbsp;первых лет своего основания.
</p>

<p>
Историкам Москвы известно, что в&nbsp;древности на&nbsp;этом месте располагалось языческое капище. По&nbsp;мнению экстрасенсов, его влияние ощущается до&nbsp;сих пор&nbsp;&mdash; не&nbsp;даром на&nbsp;магических картах Москвы Миусская площадь (или &laquo;Миуссы&raquo;) часто помечены специальным знаком &laquo;Граница миров&raquo;.
</p>

<p>
Во&nbsp;второй половине XVII века на&nbsp;месте полей у&nbsp;Тверской заставы возник лесной ряд&nbsp;&mdash; склады и&nbsp;рынки строевого леса. В&nbsp;1812 лесной ряд сгорел. Название Миусская (Миуская, Миюская) площадь впервые появляется на&nbsp;послепожарных картах 1818&nbsp;года.
</p>

<p>
На&nbsp;плане Хотева 1853 года показаны пруд и&nbsp;ручей&nbsp;&mdash; местность была заболоченная, нездоровая, овеянная нехорошими легендами. Восстановленный лесной рынок в&nbsp;1853 году занимал малую долю обширного, немощеного пространства. На&nbsp;нем торговали заборным горбылем и&nbsp;дровами, столбами и&nbsp;кругляком, обрезной доской, опилками и&nbsp;стружками, которые так нужны в&nbsp;хозяйстве. С&nbsp;юго-запада и&nbsp;северо-запада площадь была застроена мелкими дворами, с&nbsp;востока&nbsp;&mdash; к&nbsp;ней примыкали огороды. По&nbsp;мере роста капиталистической Москвы район застраивался дешевыми деревянными домами для низших слоев населения, мелкими заводами и&nbsp;свинарниками.
</p>

<p>
18&nbsp;ноября 1896&nbsp;г. Государь император высочайше соизволил дать согласие на&nbsp;учреждение Особой Строительной Комиссии по&nbsp;устроению на&nbsp;Миусской площади Московского Промышленного училища в&nbsp;память <span class="nobr">25-летия</span> царствования Государя Императора Александра II. Весь год Особая комиссия под руководством архитектора М.К. Геппенера была занята составлением проекта здания сообразно требованиям по&nbsp;учебной части и&nbsp;применительно к&nbsp;размерам и&nbsp;очертанию участка земли, отведенного на&nbsp;Миусской площади. Согласно проекту комплекс зданий Училища включал: трехэтажный училищный дом (в&nbsp;настоящее время&nbsp;&mdash; выходящая на&nbsp;Миусскую площадь часть главного корпуса РХТУ); трехэтажный дом для преподавателей и&nbsp;служителей (в&nbsp;настоящее время&nbsp;&mdash; Информационно-библиотечный центр); одноэтажный корпус для училищных служб.
</p>

<p>
Строительство было начато в&nbsp;1898 и&nbsp;завершено в&nbsp;1903&nbsp;году. Четыре тысячи рабочих строили на&nbsp;славу, и&nbsp;только из&nbsp;качественных материалов. Метровые стены возводились из&nbsp;отборного кирпича и&nbsp;каждый проходил через руки специалиста-браковщика.
</p>

