<div class="quoting">
Это хорошая традиция, но&nbsp;я&nbsp;к&nbsp;ней отношусь только как к&nbsp;традиции. Надо знать предмет, тогда все получится. Никакой &laquo;Пятачок&raquo; не&nbsp;сдаст за&nbsp;вас неорганическую химию или зачет по&nbsp;процессам и&nbsp;аппаратам.
</div>
<br>
<div class="quote">Елена&nbsp;З., студентка РХТУ</div>
<div class="quoting">
Не&nbsp;то, чтобы паломничество, но&nbsp;иногда бегаем на&nbsp;&laquo;Пятачок&raquo;, крутимся. У&nbsp;нас вообще очень загадочное здание...
</div>

<p>
Елена права&nbsp;&mdash; здание РХТУ и&nbsp;сейчас полно загадок и&nbsp;связанные с&nbsp;ним легенды возникли отнюдь не&nbsp;на&nbsp;пустом месте. Ещё в&nbsp;начале&nbsp;ХХ века обыватели считали, что в&nbsp;подвалах Промышленного и&nbsp;Ремесленного училищ живут призраки. Не&nbsp;отрицают это и&nbsp;сотрудники Службы безопасности РХТУ, которым приходится задерживаться в&nbsp;помещении до&nbsp;поздней ночи. Рассказывают о&nbsp;призраках давно умерших преподавателей, директоров, а&nbsp;то&nbsp;и&nbsp;самого Геппенера. Не&nbsp;было только упоминаний о&nbsp;появлении таинственной горбатой вещуньи, вошедшей в&nbsp;историю непознанного как Останкинская Старуха. Возможно, это просто не&nbsp;ее&nbsp;территория.
</p>

<p>
К&nbsp;сожалению, переговоры с&nbsp;ректоратом о&nbsp;проведении исследований с&nbsp;замерами энергоинформационной и&nbsp;магнитной напряженности здания не&nbsp;увенчались успехом. Однако нашей исследовательской группе тайно удалось замерить энергетический потенциал напольного орнамента на&nbsp;лестничной площадке перед Актовым залом&nbsp;им. А.Бородина при помощи биолокационной рамки. Со&nbsp;всей уверенностью можно сказать&nbsp;&mdash; энергетические показатели этого места зашкаливают. Оно наполняет мощной спокойной энергией весь парадный вестибюль и&nbsp;прилегающие к&nbsp;нему кабинеты и&nbsp;аудитории. Акустические свойства помещения великолепны&nbsp;&mdash; оно уже много лет используется для записи хоров.
</p>

<p>
<i>До&nbsp;наших дней не&nbsp;дошли ни&nbsp;одной фотографии Максима Геппенера, ни&nbsp;одного портрета&nbsp;&mdash; словно кто-то целенаправленно изымал их&nbsp;из&nbsp;архивов.</i> Не&nbsp;известно, была&nbsp;ли у&nbsp;него семья, остались&nbsp;ли потомки... До&nbsp;сегодняшнего дня здание бывшего Московского промышленного училища остается одной из&nbsp;самых загадочных его построек и&nbsp;одним из&nbsp;загадочных московских мест. Оно уже много лет привлекает любителей непознанного, которые надеются проникнуть в&nbsp;его тайны. Что спрятано под таинственным знаком на&nbsp;парадной лестнице этого вуза нам остается только гадать. Вмурован&nbsp;ли под мраморными плитами таинственный артефакт из&nbsp;Останкино, с&nbsp;веками не&nbsp;утративший силу, или зодчий все-таки избавился от&nbsp;него, а&nbsp;в&nbsp;память о&nbsp;перстне соорудил повторяющий &laquo;Знак Мироздания&raquo; каббалистический орнамент, амулет счастья, в&nbsp;одной из&nbsp;своих самых любимых и&nbsp;последних построек? Об&nbsp;этом нам не&nbsp;узнать никогда.
</p>

<p>
Единственное, что нам пока остается&nbsp;&mdash; попасть в&nbsp;РХТУ&nbsp;и, загадав желание, покрутиться на&nbsp;левой пятке в&nbsp;центре красновато-коричневого восьмиугольника. Только прежде чем загадать желание, трижды подумайте&nbsp;&mdash; а&nbsp;вдруг оно исполнится?
</p>
<br>
<b>Послесловие...</b>
<p>
Начав изучать соответствующие справочники, перечитывая воспоминания жителей &laquo;Миуссов&raquo;, мы&nbsp;узнали, что мифы и&nbsp;легенды о&nbsp;существовании подземных ходов под Миусской площадью имеют под собой реальные основания.
</p>


<div class="quote">
Вот воспоминание одного из&nbsp;фронтовиков Великой Отечественной Войны:
</div>
<div class="quoting">
В&nbsp;марте 1942 года нас, семнадцатилетних юношей, собрали в&nbsp;здании химического института на&nbsp;военные сборы перед отправкой в&nbsp;на&nbsp;фронт. Наши казармы находились в&nbsp;нижнем полуподвале. На&nbsp;занятия в&nbsp;бывший народный университет (ныне здание РГГУ) мы&nbsp;часто ходили по&nbsp;подземным ходам, которые пронизывали всю площадь. По&nbsp;подземным коммуникациям ходили все время в&nbsp;противогазах, таков был приказ командования. В&nbsp;подземных &laquo;улицах&raquo; мы&nbsp;иногда сходились в&nbsp;учебную рукопашную, ходили на&nbsp;стрельбище в&nbsp;районе заброшенного собора.
</div>

<p>
Тайный подземный ход между менделеевкой и&nbsp;бывшим собором Св. Александра Невского скорее всего и&nbsp;правда сохранился&nbsp;&mdash; по&nbsp;описаниям 1905 года он&nbsp;идёт от&nbsp;северного крыла Главного корпуса через нынешний сквер на&nbsp;Миусской площади к&nbsp;бывшему Дворцу пионэров. По&nbsp;рассказам, именно в&nbsp;нём спрятали золотые потиры, уникальные иконы в&nbsp;золотых окладах, ковчеги со&nbsp;святыми мощами и&nbsp;архиерейские наперстные кресты с&nbsp;драгоценными камнями&nbsp;&mdash; в&nbsp;соборе был один действующий придел, где всё это использовалось.
</p>

<p>
НКВД потом искало этот схрон, но&nbsp;после того, как в&nbsp;подвале этом без следа пропали двое чекистов&nbsp;&mdash; поиски прекратили...
</p>
