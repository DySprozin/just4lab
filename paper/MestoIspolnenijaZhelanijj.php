</div>

<p>
Назначение подобных символических сочетаний в&nbsp;архитектуре конца XIX&nbsp;&mdash; начала&nbsp;XX веков мы&nbsp;попытались выяснить у&nbsp;специалистов. По&nbsp;мнению члена Союза архитекторов России профессора Игоря Андронниковича Семеренко, в&nbsp;тот период считалось, что подобные знаки на&nbsp;энергетическом уровне стабилизируют обычные системы здания, успокаивают вибрации, уравновешивают полярности и&nbsp;фокусируют основную жизненную силу.
</p>

<div class="img_center">
<img alt="" src="/paper/img/magic.jpg">
</div>

<p>
В&nbsp;наше время подобная теория признана несостоятельной, но&nbsp;до&nbsp;революции широко применялась, особенно в&nbsp;архитектуре масонского толка. Тем не&nbsp;менее, такое сочетание символов, как в&nbsp;полу &laquo;Царской&raquo; лестницы бывшего Московского промышленного училища, Игорю Андронниковичу встречать не&nbsp;приходилось и&nbsp;он&nbsp;готов отнести его к&nbsp;авторскому эксклюзиву.
</p>

<p>
Но&nbsp;есть и&nbsp;более таинственная, усложненная версия: здание Московского промышленного училища строилось не&nbsp;только как учебное заведение, но&nbsp;и&nbsp;место тайных сборов для масонской ложи &laquo;Астрея&raquo; в&nbsp;помещении нынешнего Зала им. А.&nbsp;Бородина. Известно, что первый директор Училища (он&nbsp;же председатель строительной комиссии) А.А. Крылов входил в&nbsp;&laquo;Астрею&raquo;, равно как и&nbsp;третий директор (в&nbsp;период с&nbsp;1902 по&nbsp;1911&nbsp;г.), Александр Петрович Докторов, собственно принявший это здание в&nbsp;эксплуатацию в&nbsp;1903&nbsp;году.
</p>

<p>
Мы&nbsp;не&nbsp;планировали касаться в&nbsp;этой работе влияния масонства на&nbsp;политические реалии, если&nbsp;бы не&nbsp;еще одно подтверждение незримого присутствия масонов в&nbsp;Московском промышленном училище. Являясь обширной организацией пропаганды, масонство в&nbsp;начале&nbsp;ХХ века действовало путем медленного внушения, подготовляя и&nbsp;распространяя революционное брожение. Обосновавшаяся в&nbsp;Училище &laquo;Астрея&raquo; не&nbsp;отошла от&nbsp;этих традиций&nbsp;&mdash; учащиеся старших и&nbsp;младших классов и&nbsp;преподаватели чуть&nbsp;ли не&nbsp;поголовно участвовали в&nbsp;революционных событиях и&nbsp;декабрьском вооруженном восстании в&nbsp;Москве на&nbsp;Миусской площади.
</p>

Об&nbsp;этом рассказывает сфотографированный нами текст на&nbsp;мемориальной доске, которую мы&nbsp;обнаружили в&nbsp;вестибюле РХТУ: <i>&laquo;Во&nbsp;времена революции 1905 года в&nbsp;этом здании находилась большая боевая дружина, лазарет для раненных бойцов Пресненского района, склады оружия и&nbsp;мастерская по&nbsp;изготовлению взрывчатых веществ. Многие преподаватели, служащие и&nbsp;учащиеся Московского Промышленного училища участвовали в&nbsp;декабрьских революционных боях&raquo;.</i>

<p>
По&nbsp;приказу А.П. Докторова в&nbsp;1909 году над главным входом Училища был сделан барельеф с&nbsp;изображением Д.И. Менделеева&nbsp;&mdash; создателя не&nbsp;только водки и&nbsp;периодической системы, но&nbsp;и&nbsp;комиссии для изучения спиритических явлений (1875&nbsp;г.). Зятем его был Александр Блок, у&nbsp;которого были друзья масоны. Существуют сведения, что под влиянием Блока в&nbsp;1905&nbsp;году, за&nbsp;два года до&nbsp;смерти, Менделеев был принят в&nbsp;ложу &laquo;Астрея&raquo;, а&nbsp;через два года после смерти удостоился барельефа по&nbsp;месту ее&nbsp;собраний.
</p>

<p>
Изображение Менделеева помещено в&nbsp;восьмиугольник, повторяющий центр Знака Мироздания. Вокруг него&nbsp;&mdash; ленты и&nbsp;жезлы, лояльные символы позднего русского масонства московской ветви. Два сочлененные в&nbsp;один рога изобилия, скрепленные крест на&nbsp;крест масонской лентой&nbsp;&mdash; символ неисчерпаемых даров, ниспосылаемых человеку тайно и&nbsp;без его прямого содействия. Горящие факелы на&nbsp;концах рогов изобилия символизируют пылающие костры инквизиции, пламенем коих были сожжены масоны-предшественники, апологеты науки. По&nbsp;мнению директора музея РХТУ Серафима Серафимовича Аралова, это было одно из&nbsp;первых изображений Д.И. Менделеева в&nbsp;монументальном искусстве.
</p>

<p>
Перед собраниями, которые проходили под покровом ночи, братья ложи должны были вставать в&nbsp;центр Знака Мироздания для исполнения некоего мистического ритуала, подробности которого не&nbsp;дошли до&nbsp;нашего времени. Вокруг этой гипотезы сейчас ломают головы историки русского масонства. К&nbsp;версии &laquo;культового сооружения масонов&raquo; склоняется московский архитектор и&nbsp;исследователь масонских мотивов в&nbsp;архитектуре Елена Сангурская. &laquo;Мы&nbsp;знаем аргументы Сангурской,&nbsp;&mdash; сказали нам по&nbsp;телефону в&nbsp;администрации РХТУ,&nbsp;&mdash; но&nbsp;не&nbsp;считаем их&nbsp;достаточно убедительными&raquo;.
</p>

<p>
Хотя, буквально с&nbsp;первых лет существования Училища, это место породило интересную традицию&nbsp;&mdash; воспитанники считали, что оно приносит удачу. Для этого нужно встать левой пяткой в&nbsp;центр восьмиугольника и&nbsp;повернуться вокруг своей оси против часовой стрелки&nbsp;&mdash; воспитанники верили, что этот нехитрый ритуал приносит удачу на&nbsp;экзаменах. Видно, что центр гранитного восьмиугольника изрядно истерт десятками тысяч подошв.
</p>

<p>
Среди воспитанников училища также ходило поверие: если в&nbsp;полночь встать на&nbsp;это место, то&nbsp;можно вспомнить, кем ты&nbsp;был в&nbsp;прошлой жизни&nbsp;&mdash; в&nbsp;голове возникают разные картины, а&nbsp;окружающее пространство как&nbsp;бы растворяется.
</p>

<p>
Легенда легендой, но&nbsp;на&nbsp;сколько жива она в&nbsp;наше время? Мы&nbsp;решили расспросить об&nbsp;этом выпускников РХТУ разных лет и&nbsp;современных студентов. То, что мы&nbsp;услышали&nbsp;&mdash; ошеломило.
</p>
<br>
<div class="quote">Елена&nbsp;П., выпускница 1986 года</div>
<div class="quoting">
У&nbsp;нашего курса была традиция&nbsp;&mdash; крутиться на&nbsp;пятке перед экзаменами перед МАЗом&nbsp;&mdash; Малым актовым залом. Это место так у&nbsp;нас и&nbsp;называлось&nbsp;&mdash; &laquo;Пятачок&raquo;. Но&nbsp;не&nbsp;от&nbsp;пяти копеек на&nbsp;метро, а&nbsp;как&nbsp;бы от&nbsp;слова пятка. И&nbsp;девушки крутились, и&nbsp;парни.
</div>
<br>
<div class="quote">Александр&nbsp;Д., выпускник 1989&nbsp;года, преподаватель РХТУ</div>
<div class="quoting">
Да, было дело. В&nbsp;основном девчонки туда бегали, но&nbsp;иногда и&nbsp;ребят тащили. Забавно&nbsp;&mdash; экзамены после этого сдавались влет. Мы&nbsp;потом приходили на&nbsp;&laquo;Пятачок&raquo; и&nbsp;тайно поливали его пивом&nbsp;&mdash; в&nbsp;благодарность. За&nbsp;что нам часто попадало от&nbsp;уборщицы. Сейчас иногда вижу&nbsp;&mdash; ребята тоже туда бегают. Жива традиция!
</div>
<br>
