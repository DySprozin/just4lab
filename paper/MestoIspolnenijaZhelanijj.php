<p>
Среди студентов РХТУ ходит легенда про до&nbsp;сих пор сохранившийся ход под землей, который соединял когда-то здание Московского промышленного училища с&nbsp;разрушенным большевиками Собором Св. Александра Невского. Московские краеведы всерьез не&nbsp;воспринимают эту легенду&nbsp;&mdash; очевидно, что делать путь под землей между столь различными по&nbsp;назначению объектами не&nbsp;имело никакого смысла.
</p>

<p>
Однако, в&nbsp;Союзе архитекторов Москвы совершенно уверены в&nbsp;том, что все постройки Миусской площади того периода были связаны между собой единой системой подземных коммуникаций, интегрированной в&nbsp;густую сеть старинных московских подземелий. Назначение ее&nbsp;вызывает споры специалистов. По&nbsp;свидетельствам одного пожилого преподавателя РХТУ, железную дверь в&nbsp;одном из&nbsp;подвалов, ведущую в&nbsp;подземный ход на&nbsp;запад, где тогда еще стоял заброшенный собор Александра Невского, он&nbsp;обнаружил еще в&nbsp;начале <span class="nobr">50-х</span> годов прошлого столетия. Но&nbsp;проникнуть за&nbsp;нее не&nbsp;удалось...
</p>

<p>
Встретилась и&nbsp;нам в&nbsp;одном из&nbsp;темных тупиков, ведущих в&nbsp;западном направлении, таинственная низкая дверца, замки и&nbsp;скобы которой нещадно изъедены ржавчиной. Какой &laquo;сим-сим&raquo; открывают эти створки, неизвестно.
</p>

