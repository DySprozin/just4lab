<p>
Один известный диггер, знаток подземной Москвы, рассказывает, что под РХТУ есть вход тоннели метро: <i>&laquo;Мы&nbsp;пробирались по&nbsp;старинным узким проходам под РХТУ им. Менделеева. Рядом с&nbsp;нами была вентиляционная шахта кольцевой линии метро (она как раз проходит под территорией вуза)&nbsp;&mdash; мы&nbsp;слышали шум проходящих поездов&raquo;.</i>
</p>

<p>
В&nbsp;северном секторе территории РХТУ действительно возвышается вентиляционная башня Московского метрополитена. Но, вернемся на&nbsp;поверхность, в&nbsp;главный вестибюль. В&nbsp;вестибюле располагается богатая Парадная лестница, отделанная ценными породами мрамора и&nbsp;гранита. Перила украшены кованным бронзовым орнаментом работы мастера П.&nbsp;Гальвестона. Именно по&nbsp;этой лестнице поднималась Императорская семья и&nbsp;до&nbsp;революции ее&nbsp;действительно называли &laquo;Царской&raquo;&nbsp;&mdash; этот факт нам подтвердили в&nbsp;музее РХТУ.
</p>

