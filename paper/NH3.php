<p>
Мною была взята навеска хлорида аммония (0,5000&nbsp;г), перенесена количественно в&nbsp;мерную колбу и&nbsp;растворена в&nbsp;1&nbsp;л дистиллированной воды. Из&nbsp;этого раствора были получены растворы с&nbsp;концентрацией 1, 2, 5, 10&nbsp;и&nbsp;50&nbsp;мг/л. Затем, в&nbsp;соответствии с&nbsp;методикой отобрали по&nbsp;10&nbsp;мл каждой пробы в&nbsp;пробирки в&nbsp;нескольких повторностях, после чего добавили в&nbsp;каждую пробу, включая пробу с&nbsp;чистой дистиллированной водой, по&nbsp;4&nbsp;капли <span class="nobr">1-го</span> реагента, взболтали каждую пробирку на&nbsp;шейкере, по&nbsp;4&nbsp;капли <span class="nobr">2-го</span> раствора, взболтали каждую пробирку на&nbsp;шейкере, после этого добавили 4&nbsp;капли <span class="nobr">3-го</span> раствора и&nbsp;операцию повторили. Для получения цвета необходимо подождать <span class="nobr">5&ndash;7</span> минут согласно методике. Основной плюс НИЛПы от&nbsp;остальных в&nbsp;том что тут отсутствует сыпучий реактив, что решает проблему дозировки растворов и&nbsp;положительно влияет на&nbsp;точность результатов.
</p>

