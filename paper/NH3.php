<p>
В&nbsp;обоих случаях слева направо концентрация ионов NH<span class="sub1">4</span><span class="sup2">+</span>&nbsp;в мг/л: 500, 50, 10, 5, 5, 5, 2, 1, 1, фоновая проба. 
</p>
<p>
В&nbsp;теории, полученные графики калибровки должны были иметь линейную зависимость, но&nbsp;только у&nbsp;НИЛПЫ мне удалось её&nbsp;добиться с&nbsp;первого раза, стальные&nbsp;же тесты имеют больной зуб в&nbsp;сыпучем реагенте. К&nbsp;тому&nbsp;же, у&nbsp;Ser&rsquo;ы ложечка имеет не&nbsp;самую удобную форму для дозировки. Остальные тесты показали достаточную точность, но&nbsp;лишь на&nbsp;глаз, этого достаточно чтобы использовать их&nbsp;в&nbsp;аквариумистике, но&nbsp;не&nbsp;для меня. Так что лично я&nbsp;выбираю НИЛПУ&nbsp;&mdash; достаточная точность за&nbsp;разумную цену вопроса. Ниже для нее представлен калибровочный график и&nbsp;коэффициент пересчета аммонийного азота (по&nbsp;оси абсцисс&nbsp;&mdash; оптическая плотность; по&nbsp;оси ординат&nbsp;&mdash; концентрация ионов аммония, мг/л). Калибровочный коэффициент для пересчета аммонийного азота составил примерно 7,8. Т.е. для получения концентрации NH<span class="sub1">4</span><span class="sup2">+</span> ионов в&nbsp;растворе, нужно калибровочный коэффициент умножить на&nbsp;значение оптической плотности, полученное в&nbsp;ходе измерений.
</p>

