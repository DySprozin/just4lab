<p>
В&nbsp;своей научной работе я&nbsp;столкнулся с&nbsp;проблемой быстрого и&nbsp;точного определения концентрации ионов аммония. На&nbsp;выбор было несколько готовых тест систем, а&nbsp;так&nbsp;же гостированная методика. Просьба ВЖХ не&nbsp;предлагать, так как такого достижения техники в&nbsp;зоне доступа обнаружено не&nbsp;было, а&nbsp;для покупки готового прибора не&nbsp;хватало каких то&nbsp;тысяч пять.
</p>
<p>
<img class="left" src="/paper/img/nilpa.jpg" alt=""> В&nbsp;зоомагазине мне на&nbsp;глаза попалось несколько тест-систем. Мой научный руководитель настоятельно рекомендовал использовать тест-систему Sera test, цена которой составляла порядка 550&nbsp;руб. Кроме этого были приобретены тест-системы фирмы Elos Primaline (цена около 500&nbsp;руб.), а&nbsp;так&nbsp;же отечественная НИЛПА стоимостью в&nbsp;районе <span class="nobr">150&ndash;200 руб.</span> Концентрации ионов NH<span class="sub1">4</span><span class="sup2">+</span> планировалось определять в&nbsp;фугате сточных вод.
</p>
<p>
Для проведения опытов будем использовать соль NH<span class="sub1">4</span>&nbsp;Cl (с&nbsp;ещё даже почти не&nbsp;истекшим сроком годности, но&nbsp;в&nbsp;конце концов что ей&nbsp;будет). Также нам потребуется некоторое количество свежего дистиллята, пипетки, мерные колбы, пробирки, шейкер, аналитические весы с&nbsp;точностью до&nbsp;4&nbsp;знака после запятой и&nbsp;ФЭК с&nbsp;заранее подобранной длиной волны.
</p>
