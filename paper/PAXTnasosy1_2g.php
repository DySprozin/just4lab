<p> 
Поршень состоит из&nbsp;двух частей: узкой (она уже трубы насоса!) и&nbsp;широкой.
Узкая часть в&nbsp;данном случае выполнена из&nbsp;пластика, а&nbsp;широкая&nbsp;&mdash; это подвижная резиновая прокладка, покрытая маслом.
Она имеет форму кольца.
Когда мы&nbsp;опускаем поршень насоса, резиновая часть прижимается к&nbsp;пластмассовой и&nbsp;воздух нагнетается (рис.&nbsp;2).
Когда&nbsp;же мы&nbsp;поршень поднимаем (рис.&nbsp;3), прокладка-кольцо отходит, герметичность нарушается и&nbsp;воздух с&nbsp;верху поршня врывается в&nbsp;образовавшийся вакуум (он&nbsp;образуется, потому что воздух уже закачался в&nbsp;шину велосипеда и&nbsp;там остался).
</p>
<p>
Таким образом, прокладка играет роль первого клапана (снизу вверх воздух не&nbsp;проходит, а&nbsp;сверху вниз&nbsp;&mdash; проходит).
А&nbsp;что у&nbsp;нас со&nbsp;вторым клапаном? Давайте вспомним, как крепится насос к&nbsp;колесу.
Правильно, через ниппель, то&nbsp;есть полую трубку с&nbsp;резьбой и&nbsp;клапаном, упрощенно выглядит так (рис.&nbsp;4):
</p>
