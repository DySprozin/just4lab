<p>Как вы&nbsp;помните, у&nbsp;любого поршневого/плунжерного насоса есть минимум два клапана: нагнетающий и&nbsp;всасывающий.
Найдем их. Первый клапан следует искать внутри корпуса и&nbsp;не&nbsp;где-нибудь, а&nbsp;на... поршне:
</p>
