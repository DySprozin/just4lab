<p>
А&nbsp;теперь самое интересное: в&nbsp;зависимости от&nbsp;шланга насоса, ниппель может играть роль второго насосного клапана, а&nbsp;может на&nbsp;время закачки воздуха быть просто отверстием.
Разницу очень легко понять на&nbsp;самом бытовом уровне: насосом со&nbsp;шлангом первого типа вы&nbsp;не&nbsp;накачаете ничего без ниппеля, а&nbsp;со&nbsp;шлангом второго типа можно накачивать что угодно&nbsp;&mdash; хоть пакеты.
В&nbsp;чем различие? 
</p>
<p>
Упрощенная схема шланга первого типа изображена на&nbsp;рис.&nbsp;5. Привинчивая этот шланг к&nbsp;ниппелю, мы&nbsp;оставляем последний закрытым. Когда&nbsp;же мы&nbsp;начинаем опускать поршень насоса, давление над ниппелем возрастает и&nbsp;он&nbsp;открывается, пуская в&nbsp;камеру колеса воздух (рис.&nbsp;6).
Потом мы&nbsp;поршень поднимаем и&nbsp;ниппель закрывается под давлением воздуха из&nbsp;камеры шины. Теперь, думаю, все понятно: в&nbsp;данном случае ниппель играет роль второго клапана. Но&nbsp;ведь есть еще один тип шланга...
</p>
<div class="img_center">
<img alt="" src="/paper/img/nasos2-2.gif">
</div>


<p>
Когда вы&nbsp;соединяете шланг с&nbsp;ниппелем, металлическая выступающая часть (обозначена синим) открывает его и&nbsp;ниппель уже не&nbsp;может выполнять роль клапана (т.к. он&nbsp;открыт постоянно). А&nbsp;что тогда играет роль клапана? Тут уже интересно устроена та&nbsp;часть шланга, которая крепится к&nbsp;насосу: внутри нее находится шарик, а&nbsp;сама крепежная часть (трубка) устроена так, что шарик пропускает воздух только в&nbsp;одном направлении (см. рисунок)&nbsp;&mdash; вот вам и&nbsp;клапан! Соответственно, когда поршень опускается, воздух попадает в&nbsp;шину, а&nbsp;когда поднимается, шарик закрывает отверстие под давлением воздуха из&nbsp;камеры шины. 
Думаю, теперь вы&nbsp;без труда разберетесь, как работает именно ваш велосипедный насос.
</p>
<div class="img_center">
<img alt="" src="/paper/img/nippel.JPG">
</div>

<p>
На&nbsp;досуге подумайте: почему у&nbsp;автомобильных насосов встречается в&nbsp;основном первый, а&nbsp;не&nbsp;второй тип шланга? Ладно, достаточно поговорили о&nbsp;поршневых, давайте перейдем к&nbsp;центробежным, они тоже таят в&nbsp;себе много интересного.
</p>
