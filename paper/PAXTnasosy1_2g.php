<b>2. Домашняя работа :)</b>
<p>
Тааак, проверим домашку: в&nbsp;прошлый раз я&nbsp;вам предлагал узнать, как работает насос от&nbsp;вашего велосипеда.
Не&nbsp;знаю, какой у&nbsp;вас, но&nbsp;будем считать, что обычный:
</p>
