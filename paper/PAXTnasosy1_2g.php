<b>1. Два слова... или три</b>
<p>
<a href="/paper/21/165">В&nbsp;прошлой статье</a> я&nbsp;вам рассказал о&nbsp;поршневых и&nbsp;плунжерных насосах.
Судя по&nbsp;отзывам, <s>экскременты</s> эксперименты моей мозговой деятельности кому-то интересны и&nbsp;полезны, а&nbsp;мне не&nbsp;жалко: все равно я&nbsp;пишу это в&nbsp;первую очередь, чтобы разобраться самому.
</p>
<table class="mark">
<tr>
<td><img src="/paper/img/piston.gif" alt=""></td>
<td>А&nbsp;знаете&nbsp;ли вы, что поршнем называется только &laquo;блин&raquo; на&nbsp;конце &laquo;палки&raquo; (если грамотно&nbsp;&mdash; штока)? 
Учитывайте это, иначе можете попасть в&nbsp;неприятное положение, называя поршень &laquo;частью поршня&raquo;.</td>
