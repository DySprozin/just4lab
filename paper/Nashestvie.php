<b>1. Поездка-2009</b>

<p>
Мы&nbsp;приехали как раз, когда дождь уже закончился :-) Хорошо, припасли резиновые сапожки, хотя не&nbsp;долго мы&nbsp;в&nbsp;них щеголяли :-) Оказывается злостными бывают не&nbsp;только неплательщики, но&nbsp;и&nbsp;солнце! Это ужассс :-) Народ спасался как мог :-) Зонт&nbsp;&mdash; веееещь :-) Ну&nbsp;а&nbsp;так, экспромт и&nbsp;импровизация&nbsp;&mdash; все делается из&nbsp;обыкновенных картонных коробок :-) Тут уж&nbsp;кто на&nbsp;что горазд :-) И&nbsp;стену, и&nbsp;целый дом, и&nbsp;шапочку и&nbsp;платьице :-)
</p>

